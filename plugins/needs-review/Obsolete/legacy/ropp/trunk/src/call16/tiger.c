#define tiger_compress(str, state) \
  tiger_compress_macro(((dword*)str), ((dword*)state))
#else
/* The compress function is a function */
void tiger_compress(const dword *str, dword state[6])
{
    tiger_compress_macro(((dword*)str), ((dword*)state));
}
